    private void guardarArchivo(File fl) {
        String nombre=txt_Nombre.getText();
        Color array[][]=obj.getColores();
        if (nombre == null || nombre.length() == 0) {
            JOptionPane.showMessageDialog(this, "Error: Tiene que escribir un nombre para el archivo");
        } else {
            nombre += ".png";
            fl = new File(fl, nombre);
            if (SalidaEntrada.guardarTileSolo(array, fl)) {
                JOptionPane.showMessageDialog(this, "Exito al Guardar");
                obj.setFila(fl);
                this.setVisible(false);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Fracaso al guardar");
            }
        }
    }

