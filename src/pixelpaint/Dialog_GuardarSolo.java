    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (obj==null){
            JOptionPane.showMessageDialog(this, "Error: Objeto nulo");
        } else {
            int aux;
            File fl;
            JFileChooser chooser = new JFileChooser();
            chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            aux = chooser.showOpenDialog(this);
            if (JFileChooser.APPROVE_OPTION == aux) {
                fl = chooser.getSelectedFile();
                guardarArchivo(fl);
            } else {
                JOptionPane.showMessageDialog(this, "Aviso: Accion Cancelada");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


