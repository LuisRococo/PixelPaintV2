    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (control==null){
            JOptionPane.showMessageDialog(this, "Error: Control Objeto nulo");
        } else if (control.getArraySize()==0){
            JOptionPane.showMessageDialog(this, "Error: No tiene tile o nada que guardar");
        } else {
            int aux;
            File fl;
            JFileChooser chooser = new JFileChooser();
            chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            aux = chooser.showOpenDialog(this);
            if (JFileChooser.APPROVE_OPTION == aux) {
                fl = chooser.getSelectedFile();
                guardarArchivo(fl);
            } else {
                JOptionPane.showMessageDialog(this, "Aviso: Accion Cancelada");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


