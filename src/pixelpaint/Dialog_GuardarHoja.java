    private void guardarArchivo(File fl) {
        try {
            int ancho = Integer.valueOf(jTextField1.getText());
            int alto = Integer.valueOf(jTextField2.getText());
            String nombre=txt_Nombre.getText();
            int aux=alto*ancho;
            if (aux<control.getArraySize()){
                JOptionPane.showMessageDialog(this, "Error: El contenido de la sheet no cabe en las dimensiones asignadas");
            } else if (aux>=Util.Util.TAMANO_SHEET) {
                JOptionPane.showMessageDialog(this, "Error: El tama√±o limite de la sheet es de "+Util.Util.TAMANO_SHEET);
            } else if (nombre==null || nombre.length()==0){
                JOptionPane.showMessageDialog(this, "Error: Tiene que escribir un nombre para el archivo");
            }
            else {
                nombre+=".png";
                fl=new File(fl, nombre);
                if (SalidaEntrada.guardarSheet(control, ancho, alto, fl)){
                    JOptionPane.showMessageDialog(this, "Exito al Guardar");
                    this.setVisible(false);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Fracaso al guardar");
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error: No introduja Cualquier caracter no numerico");
        }
    }
    
