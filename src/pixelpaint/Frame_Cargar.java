    private void jBoton_CargarSoloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBoton_CargarSoloActionPerformed
        int aux;
        File fl;
        ControlObjetos co;
        Frame_Principal fr;
        FileNameExtensionFilter pngFilter = new FileNameExtensionFilter("Imagenes PNG", "png");
        JFileChooser chooser = new JFileChooser();
        chooser.setFileFilter(pngFilter);
        aux = chooser.showOpenDialog(this);
        if (JFileChooser.APPROVE_OPTION == aux) {
            fl = chooser.getSelectedFile();
            co=SalidaEntrada.cargarTileSolo(fl);
            if (co==null) JOptionPane.showMessageDialog(this, "ERROR: No se ha podido cargar la imagen");
            else{
                JOptionPane.showMessageDialog(this, "AVISO: Se ha podido cargar la imagen");
                fr=new Frame_Principal(co.getInfo(), co);
                fr.setLocationRelativeTo(null);
                fr.setVisible(true);
                this.setVisible(false);
                this.dispose();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Aviso: Accion Cancelada");
        }
    }//GEN-LAST:event_jBoton_CargarSoloActionPerformed

