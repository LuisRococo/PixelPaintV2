    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        byte dimWidth,dimHeight;
        try{
            dimWidth=Byte.valueOf(jTextField1.getText());
            dimHeight=Byte.valueOf(jTextField2.getText());
            if (dimWidth <1 || dimWidth > Util.Util.MAX_DIMENSION || dimHeight <1 || dimHeight > Util.Util.MAX_DIMENSION){
                JOptionPane.showMessageDialog(this, errorTxt);
            } else {
                Frame_Principal frame=new Frame_Principal(dimWidth,dimHeight);
                frame.setLocationRelativeTo(null);
                frame.setVisible(true);
                
                this.setVisible(false);
                this.dispose();
            }
        }catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, errorTxt);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

